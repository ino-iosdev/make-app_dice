
func dayOfTheWeek(day: Int) {
    
    switch day {
        
    case 1:
        print("Mon")
    case 2:
        print("T")
    case 3:
        print("W")
    case 4:
        print("TH")
    case 5:
        print("F")
    case 6:
        print("Sat")
    case 7:
        print("Sun")
    default:
        print("oops")
        
    }
  
}

dayOfTheWeek(day: 5)
